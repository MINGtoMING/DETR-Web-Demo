def imagedraw_textsize_c(draw, text, font=None):
    if int(PIL.__version__.split('.')[0]) < 10:
        tw, th = draw.textsize(text, font=font)
    else:
        left, top, right, bottom = draw.textbbox((0, 0), text, font=font)
        tw, th = right - left, bottom - top

    return tw, th


def draw_bbox(img, res_list, threshold, target_cls):
    """
    Draw bbox on image
    """
    font_path = 'simfang.ttf'
    font_size = 18
    font = ImageFont.truetype(font_path, font_size, encoding="utf-8")

    image = Image.fromarray(img)
    draw = ImageDraw.Draw(image)

    catid2color = {}
    color_list = colormap(rgb=True)[:40]
    scores, labels, boxes = res_list

    json_dt_res = {
            "label": [],
            "score": [],
            "bbox": [],
        }
    for score, catid, box in zip(scores[0], labels[0], boxes[0]):
        if score < threshold:
            continue

        if (len(target_cls) > 0) and (coco_catid2name[catid] not in target_cls):
            continue

        if catid not in catid2color:
            idx = np.random.randint(len(color_list))
            catid2color[catid] = color_list[idx]
        color = tuple(catid2color[catid])

        # draw bbox
        xmin, ymin, xmax, ymax = box
        draw.line(
            [(xmin, ymin), (xmin, ymax),
             (xmax, ymax), (xmax, ymin),
             (xmin, ymin)],
            width=2,
            fill=color)

        # draw label
        text = "{} {:.2f}".format(coco_catid2name[catid], score)
        tw, th = imagedraw_textsize_c(draw, text, font=font)
        draw.rectangle(
            [(xmin + 1, ymin - th),
             (xmin + tw + 1, ymin)],
            fill=color)
        draw.text((xmin + 1, ymin - th), text,
                  fill=(255, 255, 255), font=font)

        json_dt_res["label"].append(coco_catid2name[catid])
        json_dt_res["score"].append(score)
        json_dt_res["bbox"].append(box)

    return np.asarray(image), json_dt_res


